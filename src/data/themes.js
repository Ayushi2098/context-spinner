/**
 * Get all themes for a specific topic
 */
export function getThemesForTopic(topicId) {
  const themes = THEMES_BY_TOPIC[topicId] || [];
  return [...themes].sort((a, b) => a.name.localeCompare(b.name));
}

/**
 * Get all themes across all topics
 */
export function getAllThemes() {
  const allThemes = Object.values(THEMES_BY_TOPIC).flat();
  return [...allThemes].sort((a, b) => a.name.localeCompare(b.name));
}
